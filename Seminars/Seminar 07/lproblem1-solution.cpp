#include <iostream>
using namespace std;

// програмата трябва да дефинира масив
// и да го принтне наобратно (но има грешка)

int main () {
    int arr[8] = {15, 12, 14, 11};

    int s = sizeof(arr) / sizeof(int);
    // трябва да започнем да четем от n-1-вия елемент (в случая s-1-вия). Иначе пипаме малко извън масива
    // това е едно възможно решение
    int i = s-1;
    for(; i >= 0; --i) {
        cout<< arr[i]<< endl;
    }

    return 0;
}
